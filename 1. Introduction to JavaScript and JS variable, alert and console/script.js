// Welcome to JavaScript Basics Documentation

// 1) Displaying a simple alert
// Alert function is used to display a pop-up message
alert("Hello World!");

// Logging a message to the console
// Console.log is used to output messages to the browser console
console.log("jhjhjkajh");

// 2) Data Types
// JavaScript has several data types. Three common ones are:

// a) String: A sequence of characters enclosed in single or double quotes
let myString = "Hello, World!";

// b) Number: Numeric values, can be integers or floating-point numbers
let myNumber = 42;

// c) Boolean: Represents true or false values
let isTrue = true;
let isFalse = false;

// 3) Variables
// Variables are used to store and manage data.

// a) Declaring a variable using 'let'
let name;

// b) Assigning a value to a variable
name = "Muhammad";

// c) Reassigning a value to the variable
name = "Owais";

// d) Using 'const' to declare a constant variable
const companyName = "Example Corp";

// Note: Once a value is assigned to a 'const' variable, it cannot be reassigned.

// Displaying the value of the 'name' variable using an alert
alert(name);
